package com.example.easyeat.api.holders;

public class CategoryHolder {
    private int id;
    private String name;
    private double x;
    private double  y;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

}
